package com.apabi.r2k.msg.model;

/**
 * 报纸消息
 * @author l.wen
 */
public class PaperMsg {

	private int id;
	private String url;
	
	public PaperMsg(){}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}
}
