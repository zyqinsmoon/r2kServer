package com.apabi.r2k.common.utils;

/**
 * 分辨率实体
 * @author l.wen
 *
 */
public class Resolution {

	private int width;			//横向分辨率
	private int height;			//纵向分辨率
	
	public Resolution(){}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}
}
